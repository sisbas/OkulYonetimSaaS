export async function seedPermissions(): Promise<void> {
  const ownsConnection = !AppDataSource.isInitialized;
  const ds = AppDataSource.isInitialized ? AppDataSource : await AppDataSource.initialize();
  try {
    await ds.transaction(async (manager) => {
      const tenantSlug = process.env.SEED_TENANT_SLUG ?? 'system-seed';
      const tenantName = process.env.SEED_TENANT_NAME ?? 'System Seed Tenant';
      const tenantRows = await manager.query(
        `INSERT INTO tenants (name, slug) VALUES ($1, $2) ON CONFLICT (slug) DO UPDATE SET name = EXCLUDED.name, updated_at = now() RETURNING id`,
        [tenantName, tenantSlug],
      );
      const tenantId = tenantRows[0].id;
      for (const permission of PERMISSION_SEED) {
        await manager.query(
          `INSERT INTO permissions (code, description) VALUES ($1, $2) ON CONFLICT (code) DO UPDATE SET description = EXCLUDED.description`,
          [permission.code, permission.description],
        );
      }
      for (const roleCode of Object.keys(ROLE_PERMISSION_SEED)) {
        await manager.query(
          `INSERT INTO roles (tenant_id, name, description, is_system_role) VALUES ($1, $2, $3, true) ON CONFLICT (tenant_id, name) DO UPDATE SET description = EXCLUDED.description, is_system_role = true, updated_at = now()`,
          [tenantId, roleCode, `${roleCode} default role`],
        );
      }
      for (const [roleCode, permissionCodes] of Object.entries(ROLE_PERMISSION_SEED)) {
        const allowedPermissionCodes = [...permissionCodes];

        await manager.query(
          `DELETE FROM role_permissions rp USING roles r, permissions p WHERE rp.role_id = r.id AND rp.permission_id = p.id AND r.tenant_id = $1 AND r.name = $2 AND NOT EXISTS (SELECT 1 FROM unnest($3::varchar[]) AS allowed(code) WHERE allowed.code = p.code)`,
          [tenantId, roleCode, allowedPermissionCodes],
        );
        await manager.query(
          `INSERT INTO role_permissions (role_id, permission_id) SELECT r.id, p.id FROM roles r CROSS JOIN permissions p WHERE r.tenant_id = $1 AND r.name = $2 AND p.code = ANY($3::varchar[]) ON CONFLICT DO NOTHING`,
          [tenantId, roleCode, allowedPermissionCodes],
        );
      }
    });
    console.log(`Permissions upserted: ${PERMISSION_SEED.length}`);
    console.log('Roles ensured: 3');
    console.log(`tenant_admin permissions assigned: ${ROLE_PERMISSION_SEED.tenant_admin.length}`);
    console.log(`operations_manager permissions assigned: ${ROLE_PERMISSION_SEED.operations_manager.length}`);
    console.log(`teacher permissions assigned: ${ROLE_PERMISSION_SEED.teacher.length}`);
    console.log('Permission seed completed');
  } finally {
    if (ownsConnection && ds.isInitialized) await ds.destroy();
  }
}

if (require.main === module) {
  seedPermissions().catch((error) => {
    console.error(`Permission seed failed: ${error instanceof Error ? error.message : String(error)}`);
    process.exit(1);
  });
}
